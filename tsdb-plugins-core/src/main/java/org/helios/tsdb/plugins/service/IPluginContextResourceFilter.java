public interface IPluginContextResourceFilter {
	/**
	 * Filters resource events emitted from the plugin context
	 * @param name The name the resource was bound as
	 * @param resource The resource that was bound
	 * @return true to include, false to filter out 
	 */
	public boolean include(String name, Object resource);
}
